num = [15, 20, 25, 30, 35, 40]
count = [4, 4, 4, 4, 4, 4, 4]
for n, c in zip(num, count): 
    for i in range(c):
        np.random.seed(i)
        array = np.random.choice(range(48), size=n, replace=False)
        if 20 not in array:
            array[0] = 20
        subsets.append(array.tolist())

if invert:
    subsets = subsets[::-1]

for subset in subsets:
    assert 20 in subset

# not an efficient way to do it but its fine 
# checking that there aren't duplicated subsets
for i, j in np.ndindex((len(subsets), len(subsets))): 
    if i == j:
        continue 

    s1 = subsets[i] 
    s2 = subsets[j] 
    if len(s1) != len(s2):
        continue 
    intersect = np.intersect1d(s1, s2) 
    assert len(intersect) != len(s1)





